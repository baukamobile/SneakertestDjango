import 'package:flutter/material.dart';

class AppTextStyles {
  static TextStyle bigTextBlack() {
    return TextStyle(color: Colors.black, fontWeight: FontWeight.bold, fontSize: 30);
  }

  static TextStyle smallTextBlack() {
    return TextStyle(color: Colors.black, fontWeight: FontWeight.w500, fontSize: 20);
  }

  static TextStyle bigTextWhite() {
    return TextStyle(color: Colors.white, fontWeight: FontWeight.bold, fontSize: 30);
  }

  static TextStyle smallTextWhite() {
    return TextStyle(color: Colors.white, fontWeight: FontWeight.w500, fontSize: 20);
  }
}
